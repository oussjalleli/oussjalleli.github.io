    public function findBynom($txt)
    {
        $entityManager=$this->getEntityManager();
        $query=$entityManager
            ->createQuery('SELECT c FROM App\Entity\Sponsor c WHERE c.nom LIKE :txt or c.numContact LIKE :txt or c.email LIKE :txt or c.type LIKE :txt')
            ->setParameter('txt','%'.$txt.'%');
        return $query->getResult();
